let total = 0;
function add(x) {
  total += x;
  return total;
}
function addition(x, y) {
  total = x + y;
  return total;
}
function addsoustraction(x) {
  total -= x;
  return total;
}
function soustracition(x, y) {
  total = x - y;
  return total;
}
function addmultiply(x) {
  total *= x;
  return total;
}
function multiply(x, y) {
  total = x * y;
  return total;
}
function adddiv(x) {
  total /= x;
  return total;
}
function div(x, y) {
  total = x / y;
  return total;
}
function clear() {
  total = 0;
  return total;
}
