let total = 0;

function addition(x, y) {
  total = x + y;
  return total;
}
function add(x) {
  total += x;
  return total;
}
function addmultiply(x) {
  total *= x;
  return total;
}
function multiplication(x, y) {
  total = x * y;
  return total;
}
function adddiv(x) {
  total /= x;
  return total;
}
function div(x, y) {
  total = x / y;
  return total;
}
function clear() {
  total = 0;
  return total;
}
